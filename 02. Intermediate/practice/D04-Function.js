// function declaration
function add(a, b) {
    return a + b;
}

console.log(add(2, 3));

function subtract(a, b) {
    return a - b;
}

console.log(subtract(5, 3));

function multiply(a, b) {
    return a * b;
}

console.log(multiply(2, 3));

function divide(a, b) {
    return a / b;
}

console.log(divide(5, 3));

function power(a, b) {
    return a ** b;
}

console.log(power(2, 3));


