 /*=====  D01-Variables.js  ======*/

 // Variables are containers for storing data
 /**
 * JavaScript Variables can be declared in 4 ways
 */
//  Automatically
 // Variables are declared using the var keyword
 // Variables are declared using the let keyword
 // Variables are declared using the const keyword
 
 
 /* 1= automatically declares a variable */
 a =2;
 b=3;
 console.log(a);
 console.log(b);
 
 /* 2= declares a variable using the var keyword */
 var x = 10;
 let y = 20;
 const z = x + y;
 
 console.log(x);
 console.log(y);
 console.log(z);

 /* 3= declares a variable using the let keyword */
 let var1 = 10;
 let var2 = 20;
 let var3 = var1 + var2;
 console.log(var3);
 
 /* 4= declares a variable using the const keyword */
 const num1 = 10;       
 const num2 = 20;
 const sum = num1 + num2;
 console.log(sum);

