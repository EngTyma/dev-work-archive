// Declare an array with the "let" keyword

var fruits = ['mangoes', 'Apple', 'Banana'];

console.log(fruits[0]);

// add array value

fruits.push('Orange');
console.log(fruits);

// remove array value

fruits.pop();
console.log(fruits);

// add array value

fruits.unshift('Lemon');
console.log(fruits);

// remove array value

fruits.shift();
console.log(fruits);

